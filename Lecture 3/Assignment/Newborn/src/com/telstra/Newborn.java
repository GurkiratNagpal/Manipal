package com.telstra;
import java.util.*;
public class Newborn {
	public String name;
	private  int months;
	private double weight;
	public Newborn(String name, int months, double weight) {
		super();
		this.name = name;
		this.months = months;
		this.weight = weight;
	}
	public String getName() {
		return name;
	}

	public int getMonths() {
		return months;
	}

	public double getWeight() {
		return weight;
	}

	
	
}
